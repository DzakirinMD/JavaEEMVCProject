package mosque.model;


public class KariahBean {
	
	private String kariahic;
	private String kariahname;
	private String kariahaddress;
	private String kariahnotel;
	private String kariahemail;
	private String kariahgender;
	private String password;
	private boolean valid;
	
	public String getKariahic() {
		return kariahic;
	}
	public void setKariahic(String kariahic) {
		this.kariahic = kariahic;
	}
	public String getKariahname() {
		return kariahname;
	}
	public void setKariahname(String kariahname) {
		this.kariahname = kariahname;
	}
	public String getKariahaddress() {
		return kariahaddress;
	}
	public void setKariahaddress(String kariahaddress) {
		this.kariahaddress = kariahaddress;
	}
	public String getKariahnotel() {
		return kariahnotel;
	}
	public void setKariahnotel(String kariahnotel) {
		this.kariahnotel = kariahnotel;
	}
	public String getKariahemail() {
		return kariahemail;
	}
	public void setKariahemail(String kariahemail) {
		this.kariahemail = kariahemail;
	}
	public String getKariahgender() {
		return kariahgender;
	}
	public void setKariahgender(String kariahgender) {
		this.kariahgender = kariahgender;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public boolean isValid() {
		return valid;
	}
	public void setValid(boolean valid) {
		this.valid = valid;
	}
	

	
	
	
}
